//If else em uma linha só. Verificação simples. Operador ternário "?"

//Estrutura do Operador >> condição ? true : false

//EXEMPLO: Se idade maior que 18, pode, menor, não pode.
var idade = 15;


//Operador em ação
idade >= 18 ? console.log("Pode acessar") : console.log ("Não pode acessar");



// Uso do Operador dentro de uma variável.
var pode = idade >=18 ? true : false;

console.log(pode);

